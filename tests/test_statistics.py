def test_geometric_mean_handling():
    """Unit test for the geometric mean logic with zeros/positives."""
    # We don't need a full project for a quick logic check
    data = pd.DataFrame({
        "pos": [1, 10, 100],
        "with_zero": [0, 10, 20]
    })
    
    # Internal math check
    # Geometric mean of 1, 10, 100 is 10
    calc_gm = lambda x: np.exp(np.log(x[x > 0]).mean()) if np.any(x > 0) else np.nan
    
    assert np.isclose(calc_gm(data["pos"]), 10.0)
    # With zero, it should only consider the positive values (10, 20) -> ~14.14
    assert calc_gm(data["with_zero"]) > 10.0

def test_statistics_mcs_aggregation(project_with_sources, mocker):
    """Verify that MCS data is correctly pulled from the mcs_dict."""
    stats = Statistics(project_with_sources)
    
    # Pick one organelle to mock
    org = project_with_sources.get_organelles("mito_0001")[0]
    
    # Mock the mcs_dict property to return specific test values
    # In the real app, these are generated by MembraneContactSiteCalculator
    test_mcs_data = {
        "contact_er": 150.5,
        "contact_nucleus": 42.0
    }
    mocker.patch.object(type(org), 'mcs_dict', new_callable=mocker.PropertyMock, return_value=test_mcs_data)
    
    # Request the specific contact keys
    props = ["contact_er", "contact_nucleus", "mesh_volume"]
    df = stats.get_dataframe(ids="mito_0001", properties=props)
    
    # Verify the values were extracted correctly
    assert df.iloc[0]["contact_er"] == 150.5
    assert df.iloc[0]["contact_nucleus"] == 42.0
    
    # Verify that the summary also handles these new columns
    summary_df = stats.get_summary_dataframe(df)
    avg_row = summary_df[summary_df["Measure"] == "Average (or Share)"].iloc[0]
    assert avg_row["contact_er"] == 150.5