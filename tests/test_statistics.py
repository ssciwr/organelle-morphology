import pytest
import numpy as np
import pandas as pd
from organelle_morphology.statistics import Statistics

def test_statistics_get_dataframe_basic(project_with_sources):
    """Verify that we get a basic dataframe with default properties."""
    stats = Statistics(project_with_sources)
    df = stats.get_dataframe()
    
    assert isinstance(df, pd.DataFrame)
    assert not df.empty
    assert "ID" in df.columns
    # Default properties check
    assert "mesh_volume" in df.columns
    assert "sphericity" in df.columns
    
    # Check that ID is the first column
    assert df.columns[0] == "ID"

def test_statistics_sorting_and_filtering(project_with_sources):
    """Check that columns are sorted and ID filtering works."""
    stats = Statistics(project_with_sources)
    
    # Request properties in non-alphabetical order
    props = ["water_tight", "mesh_area", "mesh_volume"]
    df = stats.get_dataframe(ids="mito_0001", properties=props)
    
    assert df.shape[0] == 1
    assert df.iloc[0]["ID"] == "mito_0001"
    
    # Columns should be ['ID', 'mesh_area', 'mesh_volume', 'water_tight']
    expected_order = ["ID", "mesh_area", "mesh_volume", "water_tight"]
    assert list(df.columns) == expected_order

def test_statistics_summary_dataframe(project_with_sources):
    """Test the statistical summary logic, including boolean handling."""
    stats = Statistics(project_with_sources)
    
    # Ensure we have some variation in a boolean column for testing
    # (In synthetic data, water_tight is usually True)
    df_data = stats.get_dataframe(properties=["mesh_volume", "water_tight", "sphericity"])
    
    summary_df = stats.get_summary_dataframe(df_data)
    
    assert "Measure" in summary_df.columns
    assert "Average (or Share)" in summary_df["Measure"].values
    
    # Check Average row
    avg_row = summary_df[summary_df["Measure"] == "Average (or Share)"].iloc[0]
    
    # Volume should be a float
    assert isinstance(avg_row["mesh_volume"], float)
    
    # water_tight should be a float between 0 and 1 (the share)
    assert 0.0 <= avg_row["water_tight"] <= 1.0
    
    # Check that non-average measures are NaN/missing for booleans
    std_row = summary_df[summary_df["Measure"] == "Std Dev"].iloc[0]
    assert pd.isna(std_row["water_tight"])

